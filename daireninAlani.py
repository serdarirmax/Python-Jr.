# Kullanıcıdan dairenin çapını alınız ve bu dairenin alanını hesaplayınız?
####################################################################################
cap=float(input("Lutfen dairenin çapını giriniz.: "))
yariCap=cap/2
pi=3.14159
daireninAlani=pi*(yariCap*yariCap)
print("Girmiş olgunuz ölcülere göre dairenin alani: ",daireninAlani)
